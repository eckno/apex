                            <h3>Getting SerECPs</h3>
                            <p>
                            Bill and Ralph took the initiative to establish their own trading software company, initially
                             named Omega Research. Their goal was to develop and provide individuals with software tools 
                             that would empower them to trade on par with seasoned professionals. These tools were designed 
                             to bridge the gap, allowing individuals without technical or programming expertise to attain 
                             similar advantages as
                             the industry experts who relied on costly mainframe computers in institutional back offices.
                            </p>
                            <p> 
                            In 1987, Omega Research introduced System Writer, a groundbreaking desktop software product
                             that allowed users to create and back-test their own trading strategies using historical market data.
                              It also provided real-time alerts when the conditions of the strategies were met in the markets. 
                              This innovative tool revolutionized the trading industry.
                             Building upon their success, Omega Research launched System Writer Plus in 1989, incorporating 
                             cutting-edge charting features. Commodity Trader Consumer Reports likened these advancements to a 
                             monumental achievement, comparing it to "putting a man on the moon" in the realm of system trading software.
                             In 1991, Omega Research unveiled the first real-time version of their flagship product, now renamed Apex Holdings. 
                             The company reached a significant milestone three years later when they secured a licensing agreement 
                             with Dow Jones Telerate, granting them the opportunity to offer Apex Holdings as a premium service to 
                             Telerate's institutional clients worldwide. This collaboration sparked the growth of an entire industry
                              surrounding Apex Holdings' software during the 1990s. This expansion included seminars, publications, 
                              user groups, and third-party solution providers who developed numerous
                            strategies exclusively compatible with Apex Holdings for back-testing and automation purposes.
                            </p>
                            <p>
                                In 1997, Omega Research underwent an initial public offering and became publicly traded, 
                                securing a listing on the Nasdaq National Market. 
                                This significant milestone marked a new chapter in the company's journey.
                                In the year 2000, Omega Research expanded its offerings by launching an 
                                online version of its product. This move towards an online platform reflected 
                                the company's commitment to adapting to the evolving technological landscape 
                                and catering to the needs of its users in an increasingly digital era.
